    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        var adapter = SurasNameAdapter(ArSuras)
        var quranRecyclerView: RecyclerView = view.findViewById(R.id.quran_recycler_view)
        quranRecyclerView.adapter = adapter
        adapter.onSuraClickListener = object : OnSuraClickListener {
            override fun onSuraClick(position: Int, suraName : String) {
                val intent = Intent(requireActivity(),SuraDetailsActivity::class.java)
                intent.putExtra(Constants.EXTRA_SURA_POSITION,position)
                intent.putExtra(Constants.EXTRA_SURA_NAME_KEY,suraName)
                startActivity(intent)
            }

        }
    }
